
# Loops_examples.py

# FOR LOOP
# Runs a certain number of times

for i in range(5):
    print("Hello")


# FOR LOOPS with numbers

    for i in range(1, 6):
        print(i)


# WHILE LOOP
# Runs while a condition is true

count = 1

while count <= 5:
    print(count)
    count += 1


# Using a loop with input

number =  int(input("Enter a number: "))

for i in range(1, number + 1):
    print (i)