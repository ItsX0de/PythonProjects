# DATA TYPES EXAMPLES

# String (text)
text = "Hello world"
print(text)
print(type(text))

# Integer (whole number)
number = 25
print(number)
print(type(number))

# Float (decimal number)
decimal = 3.14
print(decimal)
print(type(decimal))

# Boolean (True / False)
is_coding = True 
print(is_coding)
print(type(is_coding))