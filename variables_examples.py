# VARIABLES EXAMPLES
# Variables store information that your program can use later

# String variable (text)
name = "Sean" 

# Integer variable (whole number)
age = 13

# Float variable (decimal number)
height = 1.65

# Printing variables
print(name)
print(age)
print(height)

# Printing with text
print("My name is", name)
print("I am", age, "years old")
# Variables can store anything
favourite_game = "Minecraft"
favourite_colour = "Green"

print("My favourite game is", favourite_game)
print("My favourite colour is", favourite_colour)
