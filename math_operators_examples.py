# math_operators_examples.py

# Addition
print(5 + 3)

# Subtraction
print(10 - 4)

# Multiplication
print(6 * 2)

# Division
print(10 / 2)


# Using variables

a = 8
b = 4

print(a + b)
print(a - b)
print(a * b)
print(a / b)


# Power (exponents)

print(2 ** 3)  # 2^3


# Modulus (remainder)

print(10 % 3)  # remainder after division


